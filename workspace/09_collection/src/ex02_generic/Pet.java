package ex02_generic;

public class Pet {
	//field
	private String name;
	private int age;
	public Pet(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	@Override
	public String toString() {
		return "Pet [name=" + name + ", age=" + age + "]";
	}
	
	//constructor
	
	//method
	

}
